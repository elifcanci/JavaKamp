package homework_Day2;

public class Course {
	String courseName;
	boolean completed;
	String instructor;
	String homework;
	
	
public Course() {
		
	} 

	public Course(String courseName, boolean completed, String instructor, String homework) {
		
		this.courseName = courseName;
		this.completed = completed;
		this.instructor = instructor;
		this.homework = homework;
	}

	
	
}

	
